package com.model;

public class ParkingLotBike extends A {
	String uid = "";
	String parkingLotBikeNo = "";
	String usedBy = ""; // 使用人
	String remark = "";
	

	public String getUid() {
		return uid;
	}

	public void setUid(String uid) {
		this.uid = uid;
	}

	public String getParkingLotBikeNo() {
		return parkingLotBikeNo;
	}

	public void setParkingLotBikeNo(String parkingLotBikeNo) {
		this.parkingLotBikeNo = parkingLotBikeNo;
	}

	public String getUsedBy() {
		return usedBy;
	}

	public void setUsedBy(String usedBy) {
		this.usedBy = usedBy;
	}

	public String getRemark() {
		return remark;
	}

	public void setRemark(String remark) {
		this.remark = remark;
	}

	

}

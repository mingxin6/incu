package com.model;

import java.util.ArrayList;
import java.util.List;

import org.springframework.web.multipart.MultipartFile;

import com.vo.VOFile;

public class ActivityRec {

	String uid = "";
	String title = "";
	String content = "";
	String theDate = "";
	String theUser = "";
	String status = "";

	private List<MultipartFile> files;
	private ArrayList<VOFile> fileInfo;
	
	
	public List<MultipartFile> getFiles() {
		return files;
	}

	public void setFiles(List<MultipartFile> files) {
		this.files = files;
	}

	public ArrayList<VOFile> getFileInfo() {
		return fileInfo;
	}

	public void setFileInfo(ArrayList<VOFile> fileInfo) {
		this.fileInfo = fileInfo;
	}

	public String getUid() {
		return uid;
	}

	public void setUid(String uid) {
		this.uid = uid;
	}

	public String getTitle() {
		return title;
	}

	public void setTitle(String title) {
		this.title = title;
	}

	public String getContent() {
		return content;
	}

	public void setContent(String content) {
		this.content = content;
	}

	public String getTheDate() {
		return theDate;
	}

	public void setTheDate(String theDate) {
		this.theDate = theDate;
	}

	public String getTheUser() {
		return theUser;
	}

	public void setTheUser(String theUser) {
		this.theUser = theUser;
	}

	public String getStatus() {
		return status;
	}

	public void setStatus(String status) {
		this.status = status;
	}

}

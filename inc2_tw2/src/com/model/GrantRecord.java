package com.model;

public class GrantRecord {

	String uid = "";
	String compId = "";
	String grantName = "";
	String grantIssuer = "";
	String projectName = "";//申請計畫標的
	String grantDate = "";
	String projTotalMoney = "";
	String grantMoney = "";
	String remark = "";

	public String getUid() {
		return uid;
	}

	public void setUid(String uid) {
		this.uid = uid;
	}

	public String getCompId() {
		return compId;
	}

	public void setCompId(String compId) {
		this.compId = compId;
	}

	public String getGrantName() {
		return grantName;
	}

	public void setGrantName(String grantName) {
		this.grantName = grantName;
	}

	public String getGrantIssuer() {
		return grantIssuer;
	}

	public void setGrantIssuer(String grantIssuer) {
		this.grantIssuer = grantIssuer;
	}

	public String getProjectName() {
		return projectName;
	}

	public void setProjectName(String projectName) {
		this.projectName = projectName;
	}

	public String getGrantDate() {
		return grantDate;
	}

	public void setGrantDate(String grantDate) {
		this.grantDate = grantDate;
	}

	public String getProjTotalMoney() {
		return projTotalMoney;
	}

	public void setProjTotalMoney(String projTotalMoney) {
		this.projTotalMoney = projTotalMoney;
	}

	public String getGrantMoney() {
		return grantMoney;
	}

	public void setGrantMoney(String grantMoney) {
		this.grantMoney = grantMoney;
	}

	public String getRemark() {
		return remark;
	}

	public void setRemark(String remark) {
		this.remark = remark;
	}

}
